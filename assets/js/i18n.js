  function getPageName() {
    const page = window.location.pathname.split("/").pop();
    return page || "index.html";
  }

  function hasManagedAncestor(node) {
    const parent = node.parentElement;
    return Boolean(parent && parent.closest("[data-i18n], [data-i18n-html], script, style, noscript"));
  }

  function translateExactText(lang) {
    const dictionary = exactText[lang] || {};
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
        if (hasManagedAncestor(node)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });

    const nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);

    nodes.forEach((node) => {
      const raw = node.nodeValue;
      const leading = raw.match(/^\s*/)[0];
      const trailing = raw.match(/\s*$/)[0];
      const current = raw.trim();
      if (!originalTextNodes.has(node)) originalTextNodes.set(node, current);
      const original = originalTextNodes.get(node);
      const translated = dictionary[original] || original;
      node.nodeValue = `${leading}${translated}${trailing}`;
    });
  }

  function translateExactAttributes(lang) {
    const dictionary = exactText[lang] || {};
    document.querySelectorAll("input[type='submit'], input[type='button']").forEach((element) => {
      const originalAttribute = "data-original-value";
      if (!element.hasAttribute(originalAttribute)) {
        element.setAttribute(originalAttribute, element.getAttribute("value"));
      }
      const original = element.getAttribute(originalAttribute);
      element.setAttribute("value", dictionary[original] || original);
    });
  }

  function applyLanguage(lang) {
    const dict = t[lang] || t.en;
    document.documentElement.lang = langMeta[lang].html;
    localStorage.setItem("japanTravelLang", lang);

    document.querySelectorAll("[data-i18n]").forEach((element) => {
      const key = element.getAttribute("data-i18n");
      if (dict[key]) element.textContent = dict[key];
    });

    document.querySelectorAll("[data-i18n-html]").forEach((element) => {
      const key = element.getAttribute("data-i18n-html");
      if (dict[key]) element.innerHTML = dict[key];
    });

    document.querySelectorAll("[data-lang-switch]").forEach((button) => {
      const isActive = button.getAttribute("data-lang-switch") === lang;
      button.classList.toggle("is-active", isActive);
      button.setAttribute("aria-pressed", String(isActive));
    });

    const pageTitle = titles[getPageName()];
    if (pageTitle && pageTitle[lang]) document.title = pageTitle[lang];

    translateExactText(lang);
    translateExactAttributes(lang);
  }

  function initialLanguage() {
    const saved = localStorage.getItem("japanTravelLang");
    if (saved && langMeta[saved]) return saved;
    const browserLang = navigator.language.toLowerCase();
    if (browserLang.startsWith("zh")) return "zh";
    if (browserLang.startsWith("ja")) return "ja";
    return "en";
  }

  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.getAttribute("data-lang-switch"));
    });
  });

  document.querySelectorAll(".nav-programs").forEach((item) => {
    const trigger = item.querySelector(".nav-trigger");
    if (!trigger) return;
    trigger.setAttribute("aria-haspopup", "true");
    trigger.setAttribute("aria-expanded", "false");

    trigger.addEventListener("click", (event) => {
      event.preventDefault();
      const isOpen = item.classList.toggle("is-open");
      trigger.setAttribute("aria-expanded", String(isOpen));
    });

    item.addEventListener("mouseleave", () => {
      item.classList.remove("is-open");
      trigger.setAttribute("aria-expanded", "false");
    });
  });

